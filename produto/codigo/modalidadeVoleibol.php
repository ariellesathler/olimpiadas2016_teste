<h1>Vôleibol</h1>

<div class="texto-modalidade">
  <h2>Finalidade</h2>
  <p>
      Voleibol é um esporte praticado numa quadra dividida em duas partes por uma rede, possuindo duas equipes de seis jogadores em cada lado. O objetivo da modalidade é fazer passar a bola sobre a rede de modo a que esta toque no chão dentro da quadra adversária, ao mesmo tempo que se evita que os adversários consigam fazer o mesmo.
  </p>

  <h2>Composição da equipe</h2>

  <p>
      Cada equipe de voleibol é constituída por 12 jogadores, sendo seis efetivos e seis reservas. 
  </p>

  <h2>Regras</h2>
    <p>• Uma partida de vôlei tem, normalmente, 5 sets, sem tempo definido</p>.
    <p>• Cada set é terminado quando uma equipe alcança os 25 pontos, tendo 2 pontos de vantagem sobre a equipe adversária. Caso não tenha, o set prosse até que uma equipe conquiste tal vantagem.</p>
    <p>• Cada time é composto por 6 jogadores em quadra e 6 jogadores reserva.</p>
    <p>• Após o saque, cada time só poderá tocar a bola três vezes, sendo proibido que um jogador toque a bola duas vezes seguidas.</p>
    <p>• A equipe vencedora é aquela que ganhar o maior número de sets.</p>

  <h2>Estréia nos Jogos Olímpicos</h2>

  <p>
    Sua estréia em Jogos Olímpicos foi em 1964 na cidade de Tóquio no Japão.
  </p>

  <h2>Provas</h2>
  <p>
    Masculina e feminina.
  </p>

