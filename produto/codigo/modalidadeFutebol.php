<h1>Futebol</h1>

<div class="texto-modalidade">
  <h2>Finalidade</h2>
    <p>
        O futebol é o esporte coletivo mais praticado no mundo. É disputado num campo retangular por duas equipes, de onze jogadores em cada lado, que têm como objetivo colocar a bola dentro das balizas adversárias, o que é chamado de gol.
    </p>
    <p>
        Uma partida de futebol é vencida pela equipe que marcar um maior número de gols, em dois períodos de quarenta e cinco minutos, tendo um intervalo de quinze minutos de um para o outro, dando um total de jogo de noventa minutos.
    </p>

