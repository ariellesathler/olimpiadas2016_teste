  <h2>Regras</h2>
  <p>As regras do futebol foram criadas pela International Football Association Board (IFAB)  são:</p>
        <p>• Campo de jogo: O campo deve ser retangular.</p>
        <p>• Bola de Futebol: A bola deve ter de 68 a 70 cm de circunferência e pesar de 410 a 450g no início da partida. Aos jogadores, é proibido utilizar a bola com a mão, a não ser o goleiro, dentro do limite do campo. A bola não pode ser trocada sem a autorização do árbitro.</p>
        <p>• Número de jogadores: Duas equipes com 11 jogadores cada, sendo 10 na linha e um goleiro. Se uma das equipes tiver apenas 7 jogadores, a partida é adiada ou não é iniciada.</p>
        <p>• Equipamentos no Futebol: Os jogadores não poderão portar objetos que possam machucar o adversário ou a si. O jogador deve usar equipamentos básicos como a camisa, calções, chuteiras, caneleiras e as duas equipes devem ter um conjunto de equipamentos diferentes para que possam ser identificados no campo. Caso o jogador não esteja de com todos os equipamentos corretos, o árbitro deve orientá-lo a organizar o equipamento.</p>
        <p>• Árbitro de Futebol: Cada partida deve ter um árbitro, que deve ser a autoridade em campo e recebe auxílio de dois juízes de linhas, conhecidos como bandeirinhas. Ele realiza a vistoria do gramado, aplica as regras e soluciona lances duvidosos. Ele pode alterar sua decisão caso perceba que está incorreta.</p>
        <p>• Duração da Partida: O jogo dura oficialmente 90 minutos, com dois tempos de 45 minutos e um intervalo de 15 minutos. O tempo da partida pode ser prolongado caso ocorram substituições, lesão de jogadores, transporte de jogadores ou por algum motivo que interrompa a partida.</p>
        <p>• Início e reinício do jogo: Há oito maneiras de interromper a partida e reiniciá-la. O jogo começa com um pontapé de saída e deverá ser reiniciado com esse pontapé caso ocorra um gol de uma equipe. Se a bola sair e um jogador tocá-la, é concedido um arremesso lateral ao outro time que reinicia o jogo. A terceira maneira é o tiro de meta e o jogo é reiniciado com o goleiro chutando a bola para o campo. Se a equipe adversária retorna para a defesa, sem um objetivo em si, o outro time ganha um pontapé de canto e são obrigados a reiniciar o jogo do canto onde a bola saiu. Ainda há a falta que paralisa o jogo, a lesão de um atleta e o impedimento.</p>
        <p>• A bola em jogo ou fora dele: A bola estará fora de jogo quando ultrapassar completamente a linha lateral ou meta ou quando a partida for interrompida pelo árbitro. Ela está em jogo quando rebate nas traves, mas permanece no campo de jogo e quando bate nos assistentes e árbitro e não sai do interior do campo.</p>
        <p>• Gol marcado: Ocorre quando a bola ultrapassa a linha de meta por baixo do travessão.</p>
        <p>• Impedimento: é considerado impedimento o jogador que se encontra mais próximo da linha da meta contrária que a bola e o penúltimo adversário, quando está em sua própria metade do campo, quando se encontra na mesma linha que os dois últimos adversários e na mesma linha que o penúltimo adversário.</p>
        <p>• Falta no Jogo e conduta antidesportivas: é concedido pelo árbitro um tiro livre direto para o jogador que comete faltas de uma forma que imprudente ou violenta. Agarrar um adversário, chutá-lo, empurrá-lo, tocar a bola com as mãos, dentre outras. Caso ocorra uma falta desportiva recorrente ou incitação à violência ao adversário, o jogador pode ser advertido com um cartão amarelo. As faltas passíveis de cartão vermelho são a conduta violenta do jogador, impedir um gol com a mão se você não for o goleiro, empregar linguagem ofensiva e receber o segundo cartão de advertência na partida.</p>
        <p>• Pênalti: Penalidade máxima ocorrida pela falta feita por um jogador dentro de sua área. O gol é marcado diretamente, a bola é colocada no ponto penal e os demais jogadores permanecem fora da área penal.</p>

