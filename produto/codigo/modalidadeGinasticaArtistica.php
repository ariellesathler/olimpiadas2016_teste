<h1>Ginástica Artística</h1>

<div class="texto-modalidade">
  <h2>Finalidade</h2>
  
    <p>
    A ginástica artística, também conhecida como ginástica olímpica é um esporte que requer força, agilidade, coordenação, controle do corpo, flexibilidade, equilíbrio e elegância. Os ginastas realizam exercícios em aparelhos oficiais.
    </p>
    <p>
      As apresentações da ginástica artística são individuais, ainda que nas disputas por equipes, e possuem o tempo aproximado de trinta a noventa segundos de duração, são realizadas em diferentes aparelhos e exercícios, separadas em competições femininas e masculinas.
    </p>

  <h2>Composição da equipe</h2>

  <p>
      Uma equipe é formanda por uma nação com ginastas (masculidados e feminidos) classificados para os eventos. Aquela nação que não conseguir qualificar uma equipe, está apta a enviar até três competidores para representá-la.
  </p>

  <h2>Regras</h2>

    <p>
      As provas na ginástica artística são divididas em séries, que são avaliadas por nove árbitros. Dois deles avaliam o valor da partida do atleta enquanto os outros avaliam a execução dos movimentos. Um último árbitro comanda a banca.
    </p>
    <p>
          Os ginastas recebem duas notas. A primeira é de acordo com a dificuldade da série apresentada pelo atleta, e a segunda é dada de acordo com a execução da série, sendo descontados possíveis erros.
    </p>

  <h2>Estréia nos Jogos Olímpicos</h2>
  <p>
    Sua estréia em Jogos Olímpicos foi em 1896 na cidade de Atenas na Grécia, sendo um dos quatro esportes disputados desde a primeira edição das Olimpíadas da Era Moderna.
  </p>

